use bevy_cobweb_ui::prelude::*;

//-------------------------------------------------------------------------------------------------------------------

#[derive(TypeName)]
struct TestType;

//-------------------------------------------------------------------------------------------------------------------

#[test]
fn test_type_name()
{
    assert_eq!("TestType", TestType::NAME);
    assert!(match "TestType"
    {
        TestType::NAME => true,
        _ => false,
    });
    assert!(match TestType::NAME
    {
        "TestType" => true,
        _ => false,
    });
}

